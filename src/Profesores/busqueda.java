     private void tabla() {
        String nomb=jTextField1.getText();
        DefaultTableModel modelotabla=new DefaultTableModel();
        ResultSet rs=conexion.getTabla("SELECT * FROM profesores WHERE nombre LIKE '%"+nomb+"%'");
        modelotabla.setColumnIdentifiers(new Object[]{"Nombre"});
        try{
            while(rs.next()){
                modelotabla.addRow(new Object[]{rs.getString("nombre")});
            }
            jTable1.setModel(modelotabla);
        }catch(Exception e){
            System.out.println(e);
        }
    }
