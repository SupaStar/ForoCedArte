    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String nombre,correo,direccion,ante,clase,notas,carre;
        VerProfe verpr=new VerProfe();
        int tel;
        int seleccion=jTable1.rowAtPoint(evt.getPoint());
        String nomb = String.valueOf(jTable1.getValueAt(seleccion, 0));ResultSet rs;
        Connection conn;
        conn = conexion.getConnection(); //Para tener conexi√≥n a la Base de Datos.
        String sql = "SELECT * FROM profesores WHERE nombre LIKE '%"+nomb+"%'";
        try{
            Statement stmt = conn.createStatement();
            rs=stmt.executeQuery(sql);
            rs.beforeFirst();
            rs.next();
            nombre=rs.getString(2);
            tel=rs.getInt(3);
            correo=rs.getString(4);
            direccion=rs.getString(5);
            ante=rs.getString(6);
            carre=rs.getString(7);
            clase=rs.getString(8);
            notas=rs.getString(9);
            verpr.jTextField1.setText(nombre);
            verpr.jTextField2.setText(String.valueOf(tel));
            verpr.jTextField3.setText(correo);
            verpr.jTextField4.setText(direccion);
            verpr.jTextArea1.setText(ante);
            verpr.jTextField5.setText(carre);
            verpr.jTextArea2.setText(notas);
            verpr.jComboBox1.setSelectedItem(clase);
            Inicio.jDesktopPane1.add(verpr);
            verpr.toFront();
            verpr.setVisible(true);
            dispose();
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jTable1MouseClicked


